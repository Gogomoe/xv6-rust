#!/usr/bin/perl -w

# Generate usys.S, the stubs for syscalls.

print "# generated by usys.pl - do not edit\n";

$SYS_NUM = 1;

sub entry {
    my $name = shift;
    print ".global ${name}\n";
    print "${name}:\n";
    print " li a7, ${SYS_NUM}\n";
    print " ecall\n";
    print " ret\n";
    $SYS_NUM += 1;
}

# Don't change the order.
entry("fork");
entry("exit");
entry("wait");
entry("pipe");
entry("read");
entry("kill");
entry("exec");
entry("fstat");
entry("chdir");
entry("dup");
entry("getpid");
entry("sbrk");
entry("sleep");
entry("uptime");
entry("open");
entry("write");
entry("mknod");
entry("unlink");
entry("link");
entry("mkdir");
entry("close");